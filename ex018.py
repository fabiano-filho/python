from math import radians, sin, cos, tan
a = int(input('Digite um 창ngulo: '))
sen = sin(radians(a))
cos = cos(radians(a))
tan = tan(radians(a))
print('O 창ngulo de {} tem o SEN de {:.2f}'.format(a, sen))
print('O 창ngulo de {} tem o COS de {:.2f}'.format(a, cos))
print('O 창ngulo de {} tem a TAN de {:.2f}'.format(a, tan))






